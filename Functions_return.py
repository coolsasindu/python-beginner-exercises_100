# basic Functions calculation


def plus(num1, num2):
    return num1 + num2


def mul(num1, num2):
    return num1 * num2


def sub(num1, num2):
    return num1 - num2


def dev(num1, num2):
    return num1 / num2


print(plus(8, 4))
print(mul(8, 4))
print(sub(8, 4))
print(dev(8, 4))
