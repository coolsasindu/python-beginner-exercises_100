# print 1 to 10 Numbers
i = 1
while i <= 10:
    print(i)
    i += 1 # i = i +1

print("1-10 Done \n")

# print 10 to 1 Numbers
i = 10
while i >= 1:
    print(i)
    i -= 1

print("10-1 Done \n")

# print -1 to -10 Numbers
i = -1
while i >= -10:
    print(i)
    i -= 1

print("-1 to -10 Done \n")

# print 5 to 50 Numbers
i = 5
while i <= 50:
    print(i)
    i += 5

print("5 to 50 Done \n")

# print 10 to 100 Numbers
i = 0
while i <= 100:
    print(i)
    i += 10

print("0 to 100 Done")
